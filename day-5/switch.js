let time=20;

switch(true){

    case (5<=time && time<12):
        console.log("Good Morning");
        break;

    case (12<=time && time<17):
        console.log("Good Afternoon");
        break;

    case (17<=time && time<21):
        console.log("Good Evening");
        break;


    default:
            console.log("Good Night");
}


    